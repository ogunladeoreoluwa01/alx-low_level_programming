 #include <stdio.h>
/**
 * add - adds two numbers
 *
 * @: first number
 * @b: second number
 * Return: Result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts the second number from the first
 *
 * @: first number
 * @b: second number
 * Return: Result
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divides the first number by the second
 *
 * first number
 * @b: second number
 * Return: Result
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - multiplies two numbers
 *
 * @: first number
 * @b: second number
 * Return: Result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - gives the modulus of the two numbers
 *
 * @a: the first number
 * @b: the second number
 * Return: the result
 */
int mod(int a, int b)
{
	return (a % b);
}
